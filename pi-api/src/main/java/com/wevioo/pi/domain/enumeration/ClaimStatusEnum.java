package com.wevioo.pi.domain.enumeration;

/**
 * Enumeration representing the possible status values for a claim.
 * The status can be either RESOLVED or IN_PROGRESS.
 */
public enum ClaimStatusEnum {

    /**
     * Represents the status of a resolved claim.
     */
    RESOLVED,

    /**
     * Represents the status of a claim that is in progress.
     */
    IN_PROGRESS
}

